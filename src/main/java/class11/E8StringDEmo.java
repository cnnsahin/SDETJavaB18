package class11;

public class E8StringDEmo {
    public static void main(String[] args) {

        String str="Today is Saturday";
        // print the all the index of letter a from above string
        System.out.println(str.indexOf('T'));//index of T is 0
        System.out.println(str.indexOf('o'));// index of o is 1
        System.out.println(str.indexOf('S'));//index of S is 9
        System.out.println(str.indexOf('a'));
        System.out.println(str.indexOf('Z'));
    }
}
